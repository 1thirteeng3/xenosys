class MemoryManager:
    """
    Unified memory interface with L1/L2/L3 tiers.
    
    Handles:
    - Automatic tier routing
    - L1-L2-L3 sync
    - Importnce-based promotion
    - Query optimization
    """
    
    def __init__(
        self,
        l1_config: dict[str, Any] | None = None,
        l2_config: dict[str, Any] | None = None,
        l3_config: dict[str, Any] | None = None,
    ) -> None:
        self.l1 = L1Memory(**(l1_config or {}))
        self.l2 = L2Memory(**(l2_config or {}))
        self.l3 = L3Memory(**(l3_config or {}))
        
        self._importance_threshold = l1_config.get("importance_threshold", 0.7) if l1_config else 0.7
    
    async def initialize(self) -> None:
        """Initialize all memory layers."""
        await self.l2.initialize()
        await self.l3.initialize()
        logger.info("Memory manager initialized")
    
    async def write(
        self,
        content: str,
        namespace: str = "default",
        importance: float = 1.0,
        session_id: str | None = None,
        user_id: str | None = None,
        agent_id: str | None = None,
        tags: list[str] | None = None,
        category: str = "general",
    ) -> str:
        """
        Write to memory with automatic tier placement.
        
        High importance -> L1 + L2 + L3
        Medium importance -> L1 + L2 (async to L3)
        Low importance -> L1 only
        """
        entry = MemoryEntry(
            content=content,
            namespace=namespace,
            importance=importance,
            session_id=session_id,
            user_id=user_id,
            agent_id=agent_id,
            tags=tags or [],
            category=category,
        )
        
        # Always write to L1
        l1_id = await self.l1.write(entry)
        
        # L2 for important content
        if importance >= 0.5:
            try:
                await self.l2.write(entry)
            except Exception as e:
                logger.warning(f"L2 write failed: {e}")
        
        # L3 for very important content
        if importance >= self._importance_threshold:
            try:
                await self.l3.write(entry)
            except Exception as e:
                logger.warning(f"L3 write failed: {e}")
        
        return l1_id
    
    async def read(self, entry_id: str) -> MemoryEntry | None:
        """
        Read from memory, checking tiers in order.
        """
        # Try L1 first
        entry = await self.l1.read(entry_id)
        if entry:
            return entry
        
        # Try L2
        entry = await self.l2.read(entry_id)
        if entry:
            # Promote to L1
            await self.l1.write(entry)
            return entry
        
        # Try L3
        entry = await self.l3.read(entry_id)
        if entry:
            # Promote to L1 and L2
            await self.l1.write(entry)
            try:
                await self.l2.write(entry)
            except Exception:
                pass
            return entry
        
        return None
    
    async def search(
        self,
        query: str,
        namespace: str = "default",
        limit: int = 10,
        tiers: str = "all",  # "l1", "l2", "l3", "all"
    ) -> list[SearchResult]:
        """
        Search across memory tiers.
        
        Results are deduplicated and ranked by score.
        """
        all_results: dict[str, SearchResult] = {}
        
        search_tiers = []
        if tiers == "all" or tiers == "l1":
            search_tiers.append(self.l1)
        if tiers == "all" or tiers == "l2":
            search_tiers.append(self.l2)
        if tiers == "all" or tiers == "l3":
            search_tiers.append(self.l3)
        
        for tier in search_tiers:
            try:
                results = await tier.search(query, namespace, limit * 2)
                for r in results:
                    if r.entry.id not in all_results or r.score > all_results[r.entry.id].score:
                        all_results[r.entry.id] = r
            except Exception as e:
                logger.warning(f"{tier.__class__.__name__} search failed: {e}")
        
        # Sort and rank
        sorted_results = sorted(all_results.values(), key=lambda x: -x.score)
        for i, r in enumerate(sorted_results[:limit]):
            r.rank = i
        
        return sorted_results[:limit]
    
    async def get_context(
        self,
        session_id: str,
        user_id: str | None = None,
        limit: int = 20,
    ) -> list[MemoryEntry]:
        """Get recent context for a session."""
        return await self.l1.get_context(session_id, user_id, limit)
    
    async def stats(self) -> dict[str, Any]:
        """Get memory statistics for all tiers."""
        return {
            "l1": await self.l1.stats(),
            "l2": await self.l2.stats(),
            "l3": await self.l3.stats(),
        }