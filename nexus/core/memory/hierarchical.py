# L3 Memory - Episodic & Long-term
# ============================================================================

class L3Memory(MemoryLayer):
    """
    L3 Episodic & Long-term Memory
    
    - PostgreSQL-backed persistent storage
    - Conversation episode tracking
    - User preference learning
    - Cross-agent knowledge sharing
    
    Optimized for complex queries and aggregations.
    """
    
    def __init__(
        self,
        connection_string: str = "postgresql://localhost/xenosys",
        pool_size: int = 10,
        max_overflow: int = 20,
    ) -> None:
        self.connection_string = connection_string
        self.pool_size = pool_size
        self.max_overflow = max_overflow
        
        self._engine = None
        self._session_factory = None
    
    async def initialize(self) -> None:
        """Initialize L3 database."""
        try:
            from sqlalchemy.ext.asyncio import AsyncSession, create_async_engine
            from sqlalchemy.orm import sessionmaker
            from sqlalchemy import Column, String, Text, Float, Integer, DateTime, JSON
            from sqlalchemy.ext.declarative import declarative_base
            
            Base = declarative_base()
            
            # Define tables
            class MemoryRecord(Base):
                __tablename__ = "memories"
                
                id = Column(String, primary_key=True)
                content = Column(Text, nullable=False)
                embedding = Column(JSON)  # Store as JSON for compatibility
                namespace = Column(String, default="default")
                importance = Column(Float, default=1.0)
                category = Column(String, default="general")
                tags = Column(JSON)
                links = Column(JSON)
                session_id = Column(String)
                user_id = Column(String)
                agent_id = Column(String)
                created_at = Column(DateTime, default=datetime.utcnow)
                accessed_at = Column(DateTime, default=datetime.utcnow)
                access_count = Column(Integer, default=0)
            
            class EpisodeRecord(Base):
                __tablename__ = "episodes"
                
                id = Column(String, primary_key=True)
                session_id = Column(String, index=True)
                user_id = Column(String, index=True)
                started_at = Column(DateTime, default=datetime.utcnow)
                ended_at = Column(DateTime)
                duration_seconds = Column(Float, default=0)
                summary = Column(Text)
                topics = Column(JSON)
                sentiment = Column(String, default="neutral")
                message_count = Column(Integer, default=0)
                token_count = Column(Integer, default=0)
                outcome = Column(Text)
                satisfaction = Column(Float)
            
            # Create engine
            self._engine = create_async_engine(
                connection_string,
                pool_size=self.pool_size,
                max_overflow=self.max_overflow,
                echo=False,
            )
            
            # Create tables
            async with self._engine.begin() as conn:
                await conn.run_sync(Base.metadata.create_all)
            
            # Session factory
            async_session = sessionmaker(
                self._engine, class_=AsyncSession, expire_on_commit=False
            )
            self._session_factory = async_session
            
            # Store table classes for queries
            self._MemoryRecord = MemoryRecord
            self._EpisodeRecord = EpisodeRecord
            
            logger.info("L3 memory initialized")
            
        except ImportError as e:
            logger.warning(f"L3 dependencies not available: {e}")
    
    async def write(self, entry: MemoryEntry) -> str:
        """Write to L3."""
        if self._session_factory is None:
            raise RuntimeError("L3 memory not initialized")
        
        async with self._session_factory() as session:
            from sqlalchemy import insert
            
            record = {
                "id": entry.id,
                "content": entry.content,
                "embedding": entry.embedding,
                "namespace": entry.namespace,
                "importance": entry.importance,
                "category": entry.category,
                "tags": entry.tags,
                "links": entry.links,
                "session_id": entry.session_id,
                "user_id": entry.user_id,
                "agent_id": entry.agent_id,
            }
            
            await session.execute(
                insert(self._MemoryRecord).values(**record)
            )
            await session.commit()
            
        return entry.id
    
    async def read(self, entry_id: str) -> MemoryEntry | None:
        """Read from L3."""
        if self._session_factory is None:
            return None
        
        async with self._session_factory() as session:
            from sqlalchemy import select
            
            result = await session.execute(
                select(self._MemoryRecord).where(
                    self._MemoryRecord.id == entry_id
                )
            )
            record = result.scalar_one_or_none()
            
            if not record:
                return None
            
            return MemoryEntry(
                id=record.id,
                content=record.content,
                embedding=record.embedding,
                namespace=record.namespace,
                importance=record.importance,
                category=record.category,
                tags=record.tags or [],
                links=record.links or [],
                session_id=record.session_id,
                user_id=record.user_id,
                agent_id=record.agent_id,
            )
    
    async def search(
        self,
        query: str,
        namespace: str = "default",
        limit: int = 10,
        **kwargs: Any,
    ) -> list[SearchResult]:
        """
        Search L3.
        
        For L3, we primarily search by metadata since we may not have embeddings.
        Full semantic search would require pgvector extension.
        """
        if self._session_factory is None:
            return []
        
        async with self._session_factory() as session:
            from sqlalchemy import select, func
            
            # Text search using ILIKE
            stmt = select(self._MemoryRecord).where(
                self._MemoryRecord.namespace == namespace,
                self._MemoryRecord.content.ilike(f"%{query}%"),
            ).order_by(
                self._MemoryRecord.importance.desc(),
                self._MemoryRecord.accessed_at.desc(),
            ).limit(limit)
            
            result = await session.execute(stmt)
            records = result.scalars().all()
            
            return [
                SearchResult(
                    entry=MemoryEntry(
                        id=r.id,
                        content=r.content,
                        namespace=r.namespace,
                        importance=r.importance,
                        category=r.category,
                        tags=r.tags or [],
                        session_id=r.session_id,
                        user_id=r.user_id,
                    ),
                    score=r.importance,
                    rank=i,
                )
                for i, r in enumerate(records)
            ]
    
    async def delete(self, entry_id: str) -> bool:
        """Delete from L3."""
        if self._session_factory is None:
            return False
        
        async with self._session_factory() as session:
            from sqlalchemy import delete
            
            result = await session.execute(
                delete(self._MemoryRecord).where(
                    self._MemoryRecord.id == entry_id
                )
            )
            await session.commit()
            
            return result.rowcount > 0
    
    async def clear(self, namespace: str | None = None) -> int:
        """Clear L3."""
        if self._session_factory is None:
            return 0
        
        async with self._session_factory() as session:
            from sqlalchemy import delete
            
            if namespace:
                result = await session.execute(
                    delete(self._MemoryRecord).where(
                        self._MemoryRecord.namespace == namespace
                    )
                )
            else:
                result = await session.execute(
                    delete(self._MemoryRecord)
                )
            
            await session.commit()
            
            return result.rowcount
    
    async def stats(self) -> dict[str, Any]:
        """Get L3 statistics."""
        if self._session_factory is None:
            return {"layer": "L3", "status": "not_initialized"}
        
        async with self._session_factory() as session:
            from sqlalchemy import select, func
            
            count_result = await session.execute(
                select(func.count()).select_from(self._MemoryRecord)
            )
            memory_count = count_result.scalar()
            
            episode_result = await session.execute(
                select(func.count()).select_from(self._EpisodeRecord)
            )
            episode_count = episode_result.scalar()
            
            return {
                "layer": "L3",
                "memory_count": memory_count,
                "episode_count": episode_count,
            }
    
    # Episode management
    async def create_episode(self, episode: Episode) -> str:
        """Create a new episode."""
        if self._session_factory is None:
            raise RuntimeError("L3 memory not initialized")
        
        async with self._session_factory() as session:
            from sqlalchemy import insert
            
            await session.execute(
                insert(self._EpisodeRecord).values(
                    id=episode.id,
                    session_id=episode.session_id,
                    user_id=episode.user_id,
                    started_at=episode.started_at,
                    summary=episode.summary,
                    topics=episode.topics,
                    message_count=episode.message_count,
                )
            )
            await session.commit()
        
        return episode.id
    
    async def update_episode(self, episode: Episode) -> bool:
        """Update an episode."""
        if self._session_factory is None:
            return False
        
        async with self._session_factory() as session:
            from sqlalchemy import update
            
            result = await session.execute(
                update(self._EpisodeRecord).where(
                    self._EpisodeRecord.id == episode.id
                ).values(
                    ended_at=episode.ended_at,
                    duration_seconds=episode.duration_seconds,
                    summary=episode.summary,
                    topics=episode.topics,
                    sentiment=episode.sentiment,
                    message_count=episode.message_count,
                    token_count=episode.token_count,
                    outcome=episode.outcome,
                    satisfaction=episode.satisfaction,
                )
            )
            await session.commit()
            
            return result.rowcount > 0
    
    async def get_episodes(
        self,
        user_id: str | None = None,
        session_id: str | None = None,
        limit: int = 10,
    ) -> list[Episode]:
        """Get episodes with filters."""
        if self._session_factory is None:
            return []
        
        async with self._session_factory() as session:
            from sqlalchemy import select
            
            stmt = select(self._EpisodeRecord)
            
            if user_id:
                stmt = stmt.where(self._EpisodeRecord.user_id == user_id)
            if session_id:
                stmt = stmt.where(self._EpisodeRecord.session_id == session_id)
            
            stmt = stmt.order_by(self._EpisodeRecord.started_at.desc()).limit(limit)
            
            result = await session.execute(stmt)
            records = result.scalars().all()
            
            return [
                Episode(
                    id=r.id,
                    session_id=r.session_id,
                    user_id=r.user_id,
                    started_at=r.started_at,
                    ended_at=r.ended_at,
                    duration_seconds=r.duration_seconds,
                    summary=r.summary or "",
                    topics=r.topics or [],
                    sentiment=r.sentiment or "neutral",
                    message_count=r.message_count,
                    token_count=r.token_count,
                    outcome=r.outcome,
                    satisfaction=r.satisfaction,
                )
                for r in records
            ]


