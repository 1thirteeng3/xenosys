# L2 Memory - Semantic Store
# ============================================================================

class L2Memory(MemoryLayer):
    """
    L2 Semantic Memory
    
    - Vector embeddings for semantic search
    - ChromaDB-backed persistent storage
    - Cross-session knowledge retention
    - Importance-weighted retrieval
    
    Optimized for <100ms search latency.
    """
    
    def __init__(
        self,
        persist_directory: str = "./data/l2_memory",
        collection_name: str = "semantic",
        embedding_model: str = "sentence-transformers/all-MiniLM-L6-v2",
        top_k: int = 5,
    ) -> None:
        self.persist_directory = persist_directory
        self.collection_name = collection_name
        self.embedding_model = embedding_model
        self.top_k = top_k
        
        # ChromaDB client (lazy initialization)
        self._client = None
        self._collection = None
        self._embedder = None
        
        # Sync queue for L1-L2 transfer
        self._sync_queue: asyncio.Queue[MemoryEntry] = asyncio.Queue(maxsize=1000)
        self._sync_task: asyncio.Task[None] | None = None
    
    async def initialize(self) -> None:
        """Initialize L2 memory."""
        try:
            import chromadb
            from chromadb.config import Settings
            from sentence_transformers import SentenceTransformer
            
            # Initialize ChromaDB
            self._client = chromadb.PersistentClient(
                path=self.persist_directory,
                settings=Settings(anonymized_telemetry=False),
            )
            
            self._collection = self._client.get_or_create_collection(
                name=self.collection_name,
                metadata={"description": "XenoSys L2 Semantic Memory"},
            )
            
            # Initialize embedder
            self._embedder = SentenceTransformer(self.embedding_model)
            
            # Start sync task
            self._sync_task = asyncio.create_task(self._sync_loop())
            
            logger.info(f"L2 memory initialized: {self.persist_directory}")
            
        except ImportError as e:
            logger.warning(f"L2 dependencies not available: {e}")
            # Graceful degradation
    
    async def write(self, entry: MemoryEntry) -> str:
        """Write to L2 with embedding."""
        if self._collection is None or self._embedder is None:
            raise RuntimeError("L2 memory not initialized")
        
        # Generate embedding
        embedding = self._embedder.encode(entry.content).tolist()
        entry.embedding = embedding
        
        # Store in ChromaDB
        self._collection.add(
            ids=[entry.id],
            embeddings=[embedding],
            documents=[entry.content],
            metadatas=[{
                "namespace": entry.namespace,
                "importance": entry.importance,
                "category": entry.category,
                "tags": ",".join(entry.tags),
                "created_at": entry.created_at.isoformat(),
                "session_id": entry.session_id or "",
                "user_id": entry.user_id or "",
                "agent_id": entry.agent_id or "",
            }],
        )
        
        return entry.id
    
    async def read(self, entry_id: str) -> MemoryEntry | None:
        """Read from L2."""
        if self._collection is None:
            return None
        
        result = self._collection.get(ids=[entry_id])
        
        if not result["ids"]:
            return None
        
        return self._result_to_entry(result, 0)
    
    async def search(
        self,
        query: str,
        namespace: str = "default",
        limit: int = 10,
        importance_weight: float = 0.3,
        **kwargs: Any,
    ) -> list[SearchResult]:
        """Semantic search in L2."""
        if self._collection is None or self._embedder is None:
            return []
        
        # Embed query
        query_embedding = self._embedder.encode(query).tolist()
        
        # Search ChromaDB
        where_filter = {"namespace": namespace} if namespace != "default" else None
        
        results = self._collection.query(
            query_embeddings=[query_embedding],
            n_results=limit * 2,  # Overfetch for filtering
            where=where_filter,
        )
        
        if not results["ids"] or not results["ids"][0]:
            return []
        
        # Parse results
        search_results: list[SearchResult] = []
        for i, (entry_id, distance) in enumerate(zip(
            results["ids"][0],
            results["distances"][0],
        )):
            entry = await self.read(entry_id)
            if entry:
                # Convert distance to similarity score (0-1, higher is better)
                score = 1.0 / (1.0 + distance)
                # Blend with importance
                final_score = (1 - importance_weight) * score + importance_weight * entry.importance
                
                search_results.append(SearchResult(
                    entry=entry,
                    score=final_score,
                    rank=i,
                ))
        
        # Sort by final score
        search_results.sort(key=lambda x: -x.score)
        
        return search_results[:limit]
    
    async def delete(self, entry_id: str) -> bool:
        """Delete from L2."""
        if self._collection is None:
            return False
        
        try:
            self._collection.delete(ids=[entry_id])
            return True
        except Exception:
            return False
    
    async def clear(self, namespace: str | None = None) -> int:
        """Clear L2 collection."""
        if self._collection is None:
            return 0
        
        if namespace:
            self._collection.delete(where={"namespace": namespace})
            return 1
        
        self._collection.delete()
        return 1
    
    async def stats(self) -> dict[str, Any]:
        """Get L2 statistics."""
        if self._collection is None:
            return {"layer": "L2", "status": "not_initialized"}
        
        return {
            "layer": "L2",
            "count": self._collection.count(),
            "persist_directory": self.persist_directory,
            "embedding_model": self.embedding_model,
        }
    
    def _result_to_entry(self, result: dict, index: int) -> MemoryEntry:
        """Convert ChromaDB result to MemoryEntry."""
        meta = result["metadatas"][0][index] if result["metadatas"] else {}
        
        return MemoryEntry(
            id=result["ids"][0][index],
            content=result["documents"][0][index] if result["documents"] else "",
            namespace=meta.get("namespace", "default"),
            importance=float(meta.get("importance", 1.0)),
            category=meta.get("category", "general"),
            tags=meta.get("tags", "").split(",") if meta.get("tags") else [],
            session_id=meta.get("session_id") or None,
            user_id=meta.get("user_id") or None,
            agent_id=meta.get("agent_id") or None,
        )
    
    async def _sync_loop(self) -> None:
        """Background sync from L1 to L2."""
        while True:
            try:
                entry = await asyncio.wait_for(
                    self._sync_queue.get(),
                    timeout=5.0,
                )
                
                # Only sync important entries
                if entry.importance >= self.importance_threshold:
                    await self.write(entry)
                    
            except asyncio.TimeoutError:
                continue
            except asyncio.CancelledError:
                break
            except Exception as e:
                logger.error(f"L2 sync error: {e}")
    
    async def queue_sync(self, entry: MemoryEntry) -> None:
        """Queue entry for L1-L2 sync."""
        await self._sync_queue.put(entry)


