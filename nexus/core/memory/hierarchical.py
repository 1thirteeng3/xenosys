# ============================================================================
# Memory Types
# ============================================================================

@dataclass
class MemoryEntry:
    """A single memory entry."""
    id: str = field(default_factory=lambda: str(uuid4()))
    content: str = ""
    embedding: list[float] | None = None  # For L2 semantic search
    
    # Metadata
    created_at: datetime = field(default_factory=datetime.utcnow)
    accessed_at: datetime = field(default_factory=datetime.utcnow)
    access_count: int = 0
    
    # Classification
    namespace: str = "default"
    importance: float = 1.0  # 0-1
    category: str = "general"
    
    # Relationships
    tags: list[str] = field(default_factory=list)
    links: list[str] = field(default_factory=list)  # IDs of related entries
    
    # Source tracking
    session_id: str | None = None
    user_id: str | None = None
    agent_id: str | None = None
    
    # TTL (for L1)
    expires_at: datetime | None = None
    
    def to_dict(self) -> dict[str, Any]:
        """Serialize to dictionary."""
        return {
            "id": self.id,
            "content": self.content,
            "created_at": self.created_at.isoformat(),
            "accessed_at": self.accessed_at.isoformat(),
            "access_count": self.access_count,
            "namespace": self.namespace,
            "importance": self.importance,
            "category": self.category,
            "tags": self.tags,
            "links": self.links,
            "session_id": self.session_id,
            "user_id": self.user_id,
            "agent_id": self.agent_id,
        }
    
    @property
    def is_expired(self) -> bool:
        """Check if entry has expired."""
        if self.expires_at is None:
            return False
        return datetime.utcnow() > self.expires_at


@dataclass
class SearchResult:
    """Result from memory search."""
    entry: MemoryEntry
    score: float = 0.0
    rank: int = 0


@dataclass
class Episode:
    """An episodic memory (conversation session)."""
    id: str = field(default_factory=lambda: str(uuid4()))
    session_id: str = ""
    user_id: str = ""
    
    # Messages
    messages: list[dict[str, Any]] = field(default_factory=list)
    
    # Timestamps
    started_at: datetime = field(default_factory=datetime.utcnow)
    ended_at: datetime | None = None
    duration_seconds: float = 0.0
    
    # Summary (generated by LLM)
    summary: str = ""
    topics: list[str] = field(default_factory=list)
    sentiment: str = "neutral"
    
    # Metrics
    message_count: int = 0
    token_count: int = 0
    
    # Outcome
    outcome: str | None = None
    satisfaction: float | None = None
    
    def to_dict(self) -> dict[str, Any]:
        """Serialize to dictionary."""
        return {
            "id": self.id,
            "session_id": self.session_id,
            "user_id": self.user_id,
            "started_at": self.started_at.isoformat(),
            "ended_at": self.ended_at.isoformat() if self.ended_at else None,
            "duration_seconds": self.duration_seconds,
            "summary": self.summary,
            "topics": self.topics,
            "sentiment": self.sentiment,
            "message_count": self.message_count,
            "token_count": self.token_count,
            "outcome": self.outcome,
            "satisfaction": self.satisfaction,
        }


# ============================================================================
# Memory Layer Base
# ============================================================================

class MemoryLayer(ABC):
    """Base class for memory layers."""
    
    @abstractmethod
    async def write(self, entry: MemoryEntry) -> str:
        """Write a memory entry. Returns entry ID."""
        pass
    
    @abstractmethod
    async def read(self, entry_id: str) -> MemoryEntry | None:
        """Read a memory entry by ID."""
        pass
    
    @abstractmethod
    async def search(
        self,
        query: str,
        namespace: str = "default",
        limit: int = 10,
        **kwargs: Any,
    ) -> list[SearchResult]:
        """Search memory entries."""
        pass
    
    @abstractmethod
    async def delete(self, entry_id: str) -> bool:
        """Delete a memory entry."""
        pass
    
    @abstractmethod
    async def clear(self, namespace: str | None = None) -> int:
        """Clear memory entries."""
        pass
    
    @abstractmethod
    async def stats(self) -> dict[str, Any]:
        """Get memory statistics."""
        pass


# ============================================================================
# L1 Memory - Working Context
# ============================================================================

class L1Memory(MemoryLayer):
    """
    L1 Working Context Memory
    
    - Ultra-fast, in-memory cache
    - LRU eviction policy
    - TTL-based expiration
    - Recent conversation context
    - Current task state
    
    Optimized for <10ms access latency.
    """
    
    def __init__(
        self,
        max_size: int = 10000,
        default_ttl_seconds: int = 3600,
        importance_threshold: float = 0.5,
    ) -> None:
        self.max_size = max_size
        self.default_ttl_seconds = default_ttl_seconds
        self.importance_threshold = importance_threshold
        
        # LRU cache
        self._cache: OrderedDict[str, MemoryEntry] = OrderedDict()
        self._lock = asyncio.Lock()
        
        # Statistics
        self._hits = 0
        self._misses = 0
    
    async def write(self, entry: MemoryEntry) -> str:
        """Write to L1 cache."""
        async with self._lock:
            # Set expiration
            if entry.expires_at is None:
                from datetime import timedelta
                entry.expires_at = datetime.utcnow() + timedelta(seconds=self.default_ttl_seconds)
            
            # Check size and evict if needed
            while len(self._cache) >= self.max_size:
                self._evict_lru()
            
            # Store
            self._cache[entry.id] = entry
            self._cache.move_to_end(entry.id)
            
            return entry.id
    
    async def read(self, entry_id: str) -> MemoryEntry | None:
        """Read from L1 cache."""
        async with self._lock:
            entry = self._cache.get(entry_id)
            
            if entry is None:
                self._misses += 1
                return None
            
            # Check expiration
            if entry.is_expired:
                del self._cache[entry_id]
                self._misses += 1
                return None
            
            # Update access
            entry.accessed_at = datetime.utcnow()
            entry.access_count += 1
            self._cache.move_to_end(entry_id)
            self._hits += 1
            
            return entry
    
    async def search(
        self,
        query: str,
        namespace: str = "default",
        limit: int = 10,
        **kwargs: Any,
    ) -> list[SearchResult]:
        """
        Search L1 memory.
        
        For L1, we do simple text matching since we don't have embeddings.
        In production, this would use a fast embedding index.
        """
        results: list[tuple[MemoryEntry, float]] = []
        
        async with self._lock:
            query_lower = query.lower()
            
            for entry in self._cache.values():
                if entry.namespace != namespace:
                    continue
                if entry.is_expired:
                    continue
                
                # Simple relevance scoring
                content_lower = entry.content.lower()
                
                if query_lower in content_lower:
                    # Exact match
                    score = 1.0
                else:
                    # Partial match based on words
                    query_words = set(query_lower.split())
                    content_words = set(content_lower.split())
                    overlap = len(query_words & content_words)
                    score = overlap / max(len(query_words), 1)
                
                if score > 0:
                    results.append((entry, score))
        
        # Sort by score and importance
        results.sort(key=lambda x: -(x[1] * x[0].importance))
        
        return [
            SearchResult(entry=e, score=s, rank=i)
            for i, (e, s) in enumerate(results[:limit])
        ]
    
    async def delete(self, entry_id: str) -> bool:
        """Delete from L1 cache."""
        async with self._lock:
            if entry_id in self._cache:
                del self._cache[entry_id]
                return True
            return False
    
    async def clear(self, namespace: str | None = None) -> int:
        """Clear L1 cache."""
        async with self._lock:
            if namespace is None:
                count = len(self._cache)
                self._cache.clear()
                return count
            
            # Clear by namespace
            to_delete = [
                eid for eid, e in self._cache.items()
                if e.namespace == namespace
            ]
            for eid in to_delete:
                del self._cache[eid]
            return len(to_delete)
    
    async def stats(self) -> dict[str, Any]:
        """Get L1 statistics."""
        async with self._lock:
            total = self._hits + self._misses
            hit_rate = self._hits / total if total > 0 else 0.0
            
            return {
                "layer": "L1",
                "size": len(self._cache),
                "max_size": self.max_size,
                "hits": self._hits,
                "misses": self._misses,
                "hit_rate": hit_rate,
                "utilization": len(self._cache) / self.max_size if self.max_size > 0 else 0,
            }
    
    def _evict_lru(self) -> None:
        """Evict least recently used entry."""
        if self._cache:
            self._cache.popitem(last=False)
    
    async def get_recent(
        self,
        namespace: str = "default",
        limit: int = 10,
    ) -> list[MemoryEntry]:
        """Get most recent entries."""
        async with self._lock:
            entries = [
                e for e in self._cache.values()
                if e.namespace == namespace and not e.is_expired
            ]
            entries.sort(key=lambda e: -e.accessed_at.timestamp())
            return entries[:limit]
    
    async def get_context(
        self,
        session_id: str,
        user_id: str | None = None,
        limit: int = 20,
    ) -> list[MemoryEntry]:
        """Get recent context for a session."""
        async with self._lock:
            entries = [
                e for e in self._cache.values()
                if e.session_id == session_id and not e.is_expired
                and (user_id is None or e.user_id == user_id)
            ]
            entries.sort(key=lambda e: -e.created_at.timestamp())
            return entries[:limit]


